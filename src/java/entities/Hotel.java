package entities;

import java.util.List;

/**
 *
 * @author andre
 */
public class Hotel {
    private int id;
    private String name;
    private int stars;
    private List<HotelRoom> rooms;
    

    public Hotel(int id, String name, int stars, List<HotelRoom> rooms) {
        this.id = id;
        this.name = name;
        this.rooms = rooms;
        this.stars = stars;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStars() {
        return stars;
    }

    public void setStars(int stars) {
        this.stars = stars;
    }

    public List<HotelRoom> getRooms() {
        return rooms;
    }

    public void setRooms(List<HotelRoom> rooms) {
        this.rooms = rooms;
    }
    
    
    
}
