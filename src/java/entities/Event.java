package entities;

/**
 *
 * @author andre
 */
public class Event {
    private int id;
    private String type;
    private String name;
    private int price;
    private String comment;

    public Event(int id, String type, String name, int price, String comment) {
        this.id = id;
        this.type = type;
        this.name = name;
        this.price = price;
        this.comment = comment;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
    
    
}
